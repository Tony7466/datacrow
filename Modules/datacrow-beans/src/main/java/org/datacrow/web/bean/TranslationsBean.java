package org.datacrow.web.bean;

import java.io.Serializable;

import org.datacrow.core.resources.DcResources;

import jakarta.enterprise.context.SessionScoped;
import jakarta.inject.Named;

@Named
@SessionScoped
public class TranslationsBean implements Serializable {

	public TranslationsBean() {}
	
    public String getText(String id) {
        return DcResources.getCurrent().get(id);
    }
    
    public String getText(String id, String p) {
        return DcResources.getText(id, p);
    }
}
